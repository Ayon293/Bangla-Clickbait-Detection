# ── F2: Engagement + Linguistic (log-normalized) ─────────────────────────────
META = ['viewCount','likeCount','commentCount','dislikeCount',
        'feat_char_len','feat_word_count','feat_exclaim',
        'feat_question','feat_has_number','feat_caps_ratio']

def build_meta(d):
    m = d[META].copy().fillna(0)
    for c in ['viewCount','likeCount','commentCount','dislikeCount']:
        m[c] = np.log1p(m[c])          # log-normalize skewed counts
    return m.values

scaler = StandardScaler()
X_f2_train = scaler.fit_transform(build_meta(train_df))
X_f2_test  = scaler.transform(build_meta(test_df))

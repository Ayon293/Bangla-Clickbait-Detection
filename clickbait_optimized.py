"""---
## STEP 3 — Train ML Models (LR · RF · SVM × 3 feature sets = 9 experiments)
"""

def get_scores(y_true, y_pred, model_name, fs_name):
    return {
        'Model':     model_name,
        'Features':  fs_name,
        'Accuracy':  round(accuracy_score(y_true, y_pred)*100, 2),
        'Precision': round(precision_score(y_true, y_pred, average='weighted', zero_division=0)*100, 2),
        'Recall':    round(recall_score(y_true, y_pred, average='weighted', zero_division=0)*100, 2),
        'F1-Score':  round(f1_score(y_true, y_pred, average='weighted', zero_division=0)*100, 2)
    }

MODELS = {
    'Logistic Regression': LogisticRegression(C=1.0, max_iter=1000, random_state=SEED),
    'Random Forest':       RandomForestClassifier(n_estimators=100, n_jobs=-1,
                                                   random_state=SEED),   # 100 trees = faster
    'SVM':                 LinearSVC(C=1.0, max_iter=2000, random_state=SEED)
}

all_results    = []
trained_models = {}        # store best (combined) model per classifier
bert_row       = None      # filled later if BanglaBERT runs

for model_name, model in MODELS.items():
    print(f'\n━━━ {model_name} ━━━')
    for fs_name, (X_tr, X_te) in FEATURE_SETS.items():
        t0     = time.time()
        model.fit(X_tr, y_train)
        y_pred = model.predict(X_te)
        row    = get_scores(y_test, y_pred, model_name, fs_name)
        row['Time'] = f'{time.time()-t0:.1f}s'
        all_results.append(row)
        print(f'  {fs_name:<22}  Acc={row["Accuracy"]:5.2f}%  F1={row["F1-Score"]:5.2f}%  ({row["Time"]})')
        if fs_name == 'TF-IDF + Metadata':
            trained_models[model_name] = (model, y_pred.copy())

results_df = pd.DataFrame(all_results)
print('\n All ML models trained!')


RUN_BERT = True

if RUN_BERT:

    import torch
    from torch.utils.data import Dataset, DataLoader

    from transformers import (
        AutoTokenizer,
        AutoModelForSequenceClassification,
        get_linear_schedule_with_warmup
    )

    from torch.optim import AdamW

    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

    print("Device:", device)

    MODEL_NAME = "csebuetnlp/banglabert"

    tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)

    model = AutoModelForSequenceClassification.from_pretrained(
        MODEL_NAME,
        num_labels=2
    )

    model.to(device)

    print("BanglaBERT loaded successfully!")

else:
    print("BERT skipped")

if RUN_BERT:
    class ClickbaitDataset(Dataset):
        def __init__(self, texts, labels, tok, max_len=128):
            self.texts, self.labels = list(texts), list(labels)
            self.tok, self.max_len  = tok, max_len
        def __len__(self): return len(self.texts)
        def __getitem__(self, i):
            enc = self.tok(self.texts[i], max_length=self.max_len,
                           padding='max_length', truncation=True, return_tensors='pt')
            return {'input_ids':      enc['input_ids'].squeeze(),
                    'attention_mask': enc['attention_mask'].squeeze(),
                    'label':          torch.tensor(self.labels[i], dtype=torch.long)}

    # ── Use full 8000 train / 2000 test rows — NOT a sample ──────────────────
    BATCH  = 32      # safe for T4 15GB
    MAXLEN = 128

    tr_loader = DataLoader(
        ClickbaitDataset(train_df['title'].tolist(), y_train.tolist(), tokenizer, MAXLEN),
        batch_size=BATCH, shuffle=True, num_workers=2, pin_memory=True
    )
    te_loader = DataLoader(
        ClickbaitDataset(test_df['title'].tolist(), y_test.tolist(), tokenizer, MAXLEN),
        batch_size=64, shuffle=False, num_workers=2, pin_memory=True  # bigger batch for eval
    )
    print(f'Train batches: {len(tr_loader)} | Test batches: {len(te_loader)}')
    print(f'Train samples: {len(train_df):,} | Test samples: {len(test_df):,}')
else:
    print('Skipped.')

if RUN_BERT:
    EPOCHS = 3
    LR     = 2e-5

    bert = AutoModelForSequenceClassification.from_pretrained(MODEL_NAME, num_labels=2)
    bert = bert.to(device)

    # Use torch.compile if available (PyTorch 2.0+ — speeds up ~10-20%)
    try:
        bert = torch.compile(bert)
        print('torch.compile enabled')
    except Exception:
        pass

    optimizer   = AdamW(bert.parameters(), lr=LR, eps=1e-8, weight_decay=0.01)
    total_steps = len(tr_loader) * EPOCHS
    scheduler   = get_linear_schedule_with_warmup(
        optimizer,
        num_warmup_steps   = int(total_steps * 0.06),   # 6% warmup
        num_training_steps = total_steps
    )
    scaler_amp  = torch.cuda.amp.GradScaler()           # mixed precision — ~1.5x faster

    print(f' BanglaBERT training — {EPOCHS} epochs | LR={LR} | Batch={BATCH}')
    print(f'   Total steps: {total_steps} | Warmup: {int(total_steps*0.06)}\n')

    for ep in range(EPOCHS):
        bert.train()
        ep_loss, correct, total = 0.0, 0, 0
        for i, batch in enumerate(tr_loader):
            ids  = batch['input_ids'].to(device)
            mask = batch['attention_mask'].to(device)
            lbls = batch['label'].to(device)
            optimizer.zero_grad()

            with torch.cuda.amp.autocast():              # mixed precision forward
                out  = bert(input_ids=ids, attention_mask=mask, labels=lbls)

            scaler_amp.scale(out.loss).backward()
            scaler_amp.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(bert.parameters(), 1.0)
            scaler_amp.step(optimizer)
            scaler_amp.update()
            scheduler.step()

            ep_loss += out.loss.item()
            correct += (torch.argmax(out.logits,1) == lbls).sum().item()
            total   += lbls.size(0)

            if (i+1) % 50 == 0:
                print(f'  Ep {ep+1}/{EPOCHS} | Step {i+1}/{len(tr_loader)} '
                      f'| Loss: {ep_loss/(i+1):.4f} | Acc: {correct/total:.4f}')

        print(f'   Epoch {ep+1} — Avg Loss: {ep_loss/len(tr_loader):.4f} '
              f'| Train Acc: {correct/total:.4f}\n')

    print(' BanglaBERT training complete!')
else:
    print('Skipped.')

if RUN_BERT:
    bert.eval()
    bert_preds, bert_true = [], []

    with torch.no_grad():
        for batch in te_loader:
            with torch.cuda.amp.autocast():
                out = bert(input_ids=batch['input_ids'].to(device),
                           attention_mask=batch['attention_mask'].to(device))
            bert_preds.extend(torch.argmax(out.logits,1).cpu().numpy())
            bert_true.extend(batch['label'].numpy())

    bert_preds = np.array(bert_preds)
    bert_true  = np.array(bert_true)

    bert_row = get_scores(bert_true, bert_preds, 'BanglaBERT', 'Transformer')
    all_results.append(bert_row)

    print(' BanglaBERT Results:')
    for k, v in bert_row.items(): print(f'   {k}: {v}')
    print()
    print(classification_report(bert_true, bert_preds,
                                target_names=['Not Clickbait','Clickbait']))
